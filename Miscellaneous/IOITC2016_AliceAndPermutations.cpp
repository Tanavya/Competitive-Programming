const int mod = (int) 1e9+7;

int main() {
    int T;
    scanf("%d", &T);
    while (T--) {
        int N, K;
        scanf("%d %d", &N, &K);
        K = min(N, K);
        ll ans = 1;
        for (int i = 0; i < N-K; i++) {
            ans *= (K+1);
            ans %= mod;
        }
        for (int i = 1; i <= K; i++) {
            ans *= i;
            ans %= mod;
        }
        cout << ans << endl;
    }
}